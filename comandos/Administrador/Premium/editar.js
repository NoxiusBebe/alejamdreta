let permisos_user = {
  "MANAGE_MESSAGES": "✅"
}
let permisos_bot = {
  "SEND_MESSAGES": "✅",
  "EMBED_LINKS": "✅",
  "ATTACH_FILES": "✅",
  "MANAGE_MESSAGES": "✅"
}

/* ―――――――――――――――――――――――――― */
/* ⇒ EJECUCION DEL COMANDO ⇐ */
/* ―――――――――――――――――――――――――― */
module.exports = async (client, message, args) => {
  if(message.author.bot) return;

  let estructura = "__Forma de usar el comando:__ `" + client.config.prefijos[message.guild.id] + "editar [ID del mensaje] [texto nuevo]`\n" + "```js\n" + "[] -> Obligatorio\n" + "() -> Opcional\n" + "{} -> Aclaración\n" + "```";

  let f_permisos_user = [];
  let f_permisos_bot = [];

  await comprobar_permisos_usuario(client, message);
  await comprobar_permisos_bot(client, message);

  let g_permisos_user = {};
  let g_permisos_bot = {};

  let conv_permisos = require(`../../../permissions.js`)
  g_permisos_user = await conv_permisos.convertir(permisos_user)
  g_permisos_bot = await conv_permisos.convertir(permisos_bot)

  for(var i in g_permisos_user) f_permisos_user.push(`● **${i}** ➩ ${g_permisos_user[i]}`)
  for(var i in g_permisos_bot) f_permisos_bot.push(`● **${i}** ➩ ${g_permisos_bot[i]}`)


  if(message.guild.me.hasPermission("EMBED_LINKS")){
    for(var i in permisos_user) if(permisos_user[i]==="❌") return message.channel.send(new Discord.MessageEmbed().setDescription(`⛔ __**No tienes permisos suficientes**__ ⛔\n\n`+f_permisos_user.join("\n")).setColor(`#FF3D5E`))
    for(var i in permisos_bot) if(permisos_bot[i]==="❌") return message.channel.send(new Discord.MessageEmbed().setDescription(`⛔ __**No tengo permisos suficientes**__ ⛔\n\n`+f_permisos_bot.join("\n")).setColor(`#FF3D5E`))
  }
  else{
    for(var i in permisos_user) if(permisos_user[i]==="❌") return message.channel.send(`⛔ __**No tienes permisos suficientes**__ ⛔\n\n`+f_permisos_user.join("\n"))
    for(var i in permisos_bot) if(permisos_bot[i]==="❌") return message.channel.send(`⛔ __**No tengo permisos suficientes**__ ⛔\n\n`+f_permisos_bot.join("\n"))
  }

  db_premium.get(`SELECT * FROM premium WHERE servidor = '${message.guild.id}'`, async (err, filas) => {
    if(err) return console.log(err.message + ` ${message.content} ERROR detectando "premium" en "editar" en ${message.guild.id}`)
    /*if(!filas || (filas.premium===null && filas.administrador===null)){
      let embed_no = new Discord.MessageEmbed()
        .setDescription(`__**¿QUIERES LA VERSIÓN PREMIUM EN TU SERVIDOR?**__\n\nConsulta todos los tipos de suscripciones tecleando **`+client.config.prefijos[message.guild.id]+`premium** y compra la que mas te guste.\n\nNo te olvides de concretar la ID del servidor cuando realices el pago, para saber a qué servidor se le habilitará la suscripción.`)
        .attachFiles([new MessageAttachment(`${imagen_premium}/premium-administrador.png`, `premium.png`)])
        .setImage(`attachment://premium.png`)
        .setColor(`#FF3D5E`)
        .setFooter(`Si tienes cualquier duda, contacta con el servidor soporte || https://discord.gg/Hx8CZnURed`);
      return message.channel.send(embed_no)
    }*/
    let detalles = args.join(" ").split(" ")
    if(!detalles[0]) return message.channel.send(new Discord.MessageEmbed().setDescription(`:id: **¿Cuál es la ID del mensaje?**\n\n${estructura}`).setColor(`#FF3D5E`))
    if(!detalles[1]) return message.channel.send(new Discord.MessageEmbed().setDescription(`:speech_left: **¿Y el nuevo mensaje?**\n\n${estructura}`).setColor(`#FF3D5E`))

    let mensaje;
    try{mensaje = await (client.channels.resolve(message.channel.id)).messages.fetch(detalles[0])}catch(err){return message.channel.send(new Discord.MessageEmbed().setDescription(`:x: **Este mensaje no se encuentra en este canal**\n\n${estructura}`).setColor(`#FF3D5E`))}

    let nuevo_mensaje = [];
    for(var i=1 ; i<detalles.length ; i++) nuevo_mensaje.push(detalles[i])
    if(mensaje.author.id!='822947682669559829') return message.channel.send(new Discord.MessageEmbed().setDescription(`:no_entry_sign: **Si yo no soy el autor del mensaje, no puedo editarlo**\n\n${estructura}`).setColor(`#FF3D5E`))

    if(message.guild.me.hasPermission("MANAGE_MESSAGES")) await message.delete();
    return mensaje.edit(`${nuevo_mensaje.join(" ")}`)
  })
}

