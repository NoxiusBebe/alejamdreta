const Discord = require('discord.js');
const client = new Discord.Client();
const {Client, MessageAttachment} = require('discord.js');

const sqlite3 = require('sqlite3').verbose();
const db_premium = new sqlite3.Database("./memoria/db_premium.sqlite");
const db_servidores = new sqlite3.Database("./memoria/db_servidores.sqlite");

let imagen_premium = "./archivos/Privado/Alejandreta 3.0/Logos Premium";


/* ―――――――――――――――――――――――――――――――――――――――― */
/* ⇒ PERMISOS NECESARIOS (USUARIO Y BOT) ⇐ */
/* ―――――――――――――――――――――――――――――――――――――――― */
let permisos_user = {
  "SEND_MESSAGES": "✅",
  "ADMINISTRATOR": "✅"
}
let permisos_bot = {
  "SEND_MESSAGES": "✅",
  "EMBED_LINKS": "✅",
  "ATTACH_FILES": "✅"
}

/* ―――――――――――――――――――――――――― */
/* ⇒ EJECUCION DEL COMANDO ⇐ */
/* ―――――――――――――――――――――――――― */
module.exports = async (client, message, args) => {
  if(message.author.bot) return;

  let estructura = "__Forma de usar el comando:__ `" + client.config.prefijos[message.guild.id] + "+listado [invitacion] | [descripcion del servidor {máx 200 carácteres}]`\n" + "```js\n" + "[] -> Obligatorio\n" + "() -> Opcional\n" + "{} -> Aclaración\n" + "```";

  let f_permisos_user = [];
  let f_permisos_bot = [];

  await comprobar_permisos_usuario(client, message);
  await comprobar_permisos_bot(client, message);

  let g_permisos_user = {};
  let g_permisos_bot = {};

  let conv_permisos = require(`../../../permissions.js`)
  g_permisos_user = await conv_permisos.convertir(permisos_user)
  g_permisos_bot = await conv_permisos.convertir(permisos_bot)

  for(var i in g_permisos_user) f_permisos_user.push(`● **${i}** ➩ ${g_permisos_user[i]}`)
  for(var i in g_permisos_bot) f_permisos_bot.push(`● **${i}** ➩ ${g_permisos_bot[i]}`)


  if(message.guild.me.hasPermission("EMBED_LINKS")){
    for(var i in permisos_user) if(permisos_user[i]==="❌") return message.channel.send(new Discord.MessageEmbed().setDescription(`⛔ __**No tienes permisos suficientes**__ ⛔\n\n`+f_permisos_user.join("\n")).setColor(`#ACC5FB`))
    for(var i in permisos_bot) if(permisos_bot[i]==="❌") return message.channel.send(new Discord.MessageEmbed().setDescription(`⛔ __**No tengo permisos suficientes**__ ⛔\n\n`+f_permisos_bot.join("\n")).setColor(`#ACC5FB`))
  }
  else{
    for(var i in permisos_user) if(permisos_user[i]==="❌") return message.channel.send(`⛔ __**No tienes permisos suficientes**__ ⛔\n\n`+f_permisos_user.join("\n"))
    for(var i in permisos_bot) if(permisos_bot[i]==="❌") return message.channel.send(`⛔ __**No tengo permisos suficientes**__ ⛔\n\n`+f_permisos_bot.join("\n"))
  }

  db_premium.get(`SELECT * FROM premium WHERE servidor = '${message.guild.id}'`, async (err, filas) => {
    if(err) return console.log(err.message + ` ${message.content} ERROR #1 al detectar premium con ${message.guild.id}`)
    /*if(!filas || (filas.premium===null && filas.informacion===null)){
      let embed_no = new Discord.MessageEmbed()
        .setDescription(`__**¿QUIERES LA VERSIÓN PREMIUM EN TU SERVIDOR?**__\n\nConsulta todos los tipos de suscripciones tecleando **`+client.config.prefijos[message.guild.id]+`premium** y compra la que mas te guste.\n\nNo te olvides de concretar la ID del servidor cuando realices el pago, para saber a qué servidor se le habilitará la suscripción.`)
        .attachFiles([new MessageAttachment(`${imagen_premium}/premium-informacion.png`, `premium.png`)])
        .setImage(`attachment://premium.png`)
        .setColor(`#ACC5FB`)
        .setFooter(`Si tienes cualquier duda, contacta con el servidor soporte || https://discord.gg/Hx8CZnURed`);
      return message.channel.send(embed_no)
    }*/

    let datos = args.join(" ").split(" | ")
    if(!datos[0] || !datos[1]) return message.channel.send(new Discord.MessageEmbed().setDescription(`:upside_down: **Te faltan datos por aportar**\n\n${estructura}`).setColor(`#ACC5FB`))
    if(datos[1].length > 200) return message.channel.send(new Discord.MessageEmbed().setDescription(`:notepad_spiral: **Has superado el límite de 200 carácteres en la descripción de tu servidor**\n\n${estructura}`).setColor(`#ACC5FB`))

    db_servidores.get(`SELECT * FROM servidores WHERE id = '${message.guild.id}'`, async (err, filas2) => {
      if(err) return console.log(err.message + ` ${message.content} ERROR #2 en el comando "+listado"`)

      let sentencia;
      if(!filas2) sentencia = `INSERT INTO servidores(id,nombre,invitacion,descripcion) VALUES('${message.guild.id}','${message.guild.name}','${datos[0]}','${datos[1]}')`;
      else sentencia = `UPDATE servidores SET nombre = '${message.guild.name}', invitacion = '${datos[0]}', descripcion = '${datos[1]}' WHERE id = '${message.guild.id}'`;

      db_servidores.run(sentencia, function() {
        if(err) return console.log(err.message + ` ${message.content} ERROR #3 en el comando "+listado"`)
        return message.channel.send(new Discord.MessageEmbed().setDescription(`:inbox_tray: **El servidor ha sido insertado en el listado del bot**`).setColor(`#ACC5FB`))
      })

    })
  })
}

